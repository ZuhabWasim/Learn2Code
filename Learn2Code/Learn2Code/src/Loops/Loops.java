package Loops;

public class Loops {

	public static void main(String[] args) {
		
		//For loops
		for(int x = 0; x <= 6; x++){
			System.out.print(x + " ");
			
		}
		
		System.out.println();
		
		//Do whiles
		int y = 0;
		
		do{
			System.out.print(y  + " ");
			y++;
		}while(y <= 12);
		
	}
	
}
